    private void bCariPelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCariPelangganActionPerformed
       // Validasi apakah kolom ID Pelanggan sudah diisi
        if (tIdPelanggan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kolom ID Pelanggan harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int idPelanggan = Integer.parseInt(tIdPelanggan.getText());

        try {
            String sql = "SELECT * FROM pelanggan WHERE id_pelanggan = ?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setInt(1, idPelanggan);
            ResultSet res = stat.executeQuery();

            if (res.next()) {
                tNamaPelanggan.setText(res.getString("nama_pelanggan"));
                tAlamat.setText(res.getString("alamat"));
                tTelepon.setText(res.getString("telepon"));
                tEmail.setText(res.getString("email"));

                String jenisKelamin = res.getString("jenis_kelamin");
                if (jenisKelamin.equalsIgnoreCase("Laki-laki")) {
                    rbLakiLaki.setSelected(true);
                    rbPerempuan.setSelected(false);
                } else if (jenisKelamin.equalsIgnoreCase("Perempuan")) {
                    rbLakiLaki.setSelected(false);
                    rbPerempuan.setSelected(true);
                }

                tTanggalLahir.setDate(res.getDate("tanggal_lahir"));
            } else {
                JOptionPane.showMessageDialog(this, "ID Pelanggan tidak ditemukan.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                // Reset nilai pada form
                tNamaPelanggan.setText("");
                tAlamat.setText("");
                tTelepon.setText("");
                tEmail.setText("");
                rbLakiLaki.setSelected(false);
                rbPerempuan.setSelected(false);
                tTanggalLahir.setDate(null);
                tJumlahBayar.setText("");
                tKembalian.setText("");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mencari data pelanggan.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DataPembayaran.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bCariPelangganActionPerformed

    private void bCetakNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCetakNotaActionPerformed
        String tIdPembayaran  =  this.tIdPembayaran.getText();
        String path =".\\src\\laporan\\buktipembayaran.jasper";
        JasperReport reports; 
        Map<String, Object> parameters = new HashMap<>();
        parameters.put("id_pembayaran", tIdPembayaran);
        try {
            reports = (JasperReport) JRLoader.loadObjectFromFile(path);
            JasperPrint jprint = JasperFillManager.fillReport(path, parameters,conn);
            JasperViewer jviewer = new JasperViewer(jprint,false);
            jviewer.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            jviewer.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(DataPembayaran.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bCetakNotaActionPerformed

