    private boolean isFormValid() {
        // Validasi apakah semua field sudah terisi
        if (tIdPelanggan.getText().isEmpty() || tJumlahBayar.getText().isEmpty() || tKembalian.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Validasi apakah nilai jumlahBayar dan kembalian merupakan angka positif
        try {
            int jumlahBayar = Integer.parseInt(tJumlahBayar.getText());
            int kembalian = Integer.parseInt(tKembalian.getText());

            if (jumlahBayar <= 0 || kembalian < 0) {
                JOptionPane.showMessageDialog(this, "Nilai Jumlah Bayar harus lebih dari 0, dan Kembalian tidak boleh negatif.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Kolom Jumlah Bayar dan Kembalian harus diisi dengan angka.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
   
    }
