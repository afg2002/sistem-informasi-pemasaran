    private void tabPembayaranMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabPembayaranMouseClicked
        int selectedRowIndex = tabPembayaran.getSelectedRow();
        if (selectedRowIndex >= 0) {
            String idPembayaran = tabmode.getValueAt(selectedRowIndex, 0).toString();
            String idPelanggan = tabmode.getValueAt(selectedRowIndex, 1).toString();
            String namaPelanggan = tabmode.getValueAt(selectedRowIndex, 2).toString();
            String alamatPelanggan = tabmode.getValueAt(selectedRowIndex, 3).toString();
            String teleponPelanggan = tabmode.getValueAt(selectedRowIndex, 4).toString();
            String emailPelanggan = tabmode.getValueAt(selectedRowIndex, 5).toString();
            String jenisKelamin = tabmode.getValueAt(selectedRowIndex, 6).toString();
            String tanggalLahir = tabmode.getValueAt(selectedRowIndex, 7).toString();
            String jumlahBayar = tabmode.getValueAt(selectedRowIndex, 8).toString();
            String kembalian = tabmode.getValueAt(selectedRowIndex, 9).toString();

            // Set nilai ke komponen sesuai dengan data yang dipilih
            tIdPembayaran.setText(idPembayaran);
            tIdPelanggan.setText(idPelanggan);
            tNamaPelanggan.setText(namaPelanggan);
            tAlamat.setText(alamatPelanggan);
            tTelepon.setText(teleponPelanggan);
            tEmail.setText(emailPelanggan);

            if (jenisKelamin.equals("Laki-laki")) {
                rbLakiLaki.setSelected(true);
                rbPerempuan.setSelected(false);
            } else if (jenisKelamin.equals("Perempuan")) {
                rbLakiLaki.setSelected(false);
                rbPerempuan.setSelected(true);
            }
            
            tTanggalLahir.setDate(java.sql.Date.valueOf(tanggalLahir));

            tJumlahBayar.setText(jumlahBayar);
            tKembalian.setText(kembalian);
        }
    }//GEN-LAST:event_tabPembayaranMouseClicked

