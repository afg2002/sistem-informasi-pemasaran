    private void bDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteActionPerformed
        int selectedRowIndex = tabPembayaran.getSelectedRow();
    if (selectedRowIndex == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data pembayaran yang akan dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; // Menghentikan proses jika tidak ada data pembayaran yang dipilih
    }

    int idPembayaran = Integer.parseInt(tabmode.getValueAt(selectedRowIndex, 0).toString());
    int option = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data pembayaran ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (option == JOptionPane.YES_OPTION) {
        try {
            String condition = "id_pembayaran = " + idPembayaran;
            db.deleteData(conn, "pembayaran", condition);
            datatable();
            resetForm();
//            JOptionPane.showMessageDialog(this, "Data pembayaran berhasil dihapus.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(DataPembayaran.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data pembayaran.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_bDeleteActionPerformed

