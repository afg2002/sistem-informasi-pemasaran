    protected void datatable() {
    Object[] baris = {"id_mitra_kerja", "nama_manajemen_pemasaran", "telepon", "email","alamat",};
    tabmode = new DefaultTableModel(null, baris);
    tabPartnership.setModel(tabmode);
    String sql = "SELECT * FROM partnership"; // Sesuaikan dengan nama tabel partnership di database Anda
    try {
        Statement stat = conn.createStatement();
        ResultSet res = stat.executeQuery(sql);
        while (res.next()) {
            String idMitraKerja = res.getString("id_mitra_kerja");
            String namaManajemen = res.getString("nama_manajemen_pemasaran");
            String alamat = res.getString("alamat");
            String telepon = res.getString("telepon");
            String email = res.getString("email");
            String[] data = {idMitraKerja, namaManajemen, telepon, email,alamat };
            tabmode.addRow(data);
        }
    } catch (SQLException ex) {
        System.out.println("Error" + ex);
    }
}


