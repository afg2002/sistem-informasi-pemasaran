    // Method untuk memeriksa apakah semua kolom pada form partnership telah diisi
    private boolean isFormPartnershipFilled() {
        String namaManajemen = tNamaManajemen.getText();
        String alamat = tAlamat.getText();
        String email = tEmail.getText();
        String telephone = tTelephone.getText();
        

        return !namaManajemen.isEmpty() && !alamat.isEmpty() && !email.isEmpty() && !telephone.isEmpty();
    }
    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
//        String idMitra = tIdMitra.getText();
        if (!isFormPartnershipFilled()) {
         JOptionPane.showMessageDialog(this, "Semua kolom pada form partnership harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
         return; // Menghentikan proses penyisipan data ke tabel partnership karena form tidak valid
     }

     String nama_manajemen_pemasaran = tNamaManajemen.getText();
     String alamat = tAlamat.getText();
     String email = tEmail.getText();
     String telephone = tTelephone.getText();
     

     Object[] values = {"0", nama_manajemen_pemasaran, telephone, alamat, email};
     try {
         db.insertData(conn, table, columns, values);
         datatable();
     } catch (SQLException ex) {
         Logger.getLogger(dataPartnership.class.getName()).log(Level.SEVERE, null, ex);
     }
    }//GEN-LAST:event_bSaveActionPerformed

    private void bDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteActionPerformed
            String idMitraKerja = tIdMitra.getText();

            // Validasi apakah ID Mitra Kerja sudah diisi
            if (idMitraKerja.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Kolom ID Mitra Kerja harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                return; // Menghentikan proses penghapusan data dari tabel partnership
            }

            // Konfirmasi penghapusan data
            int confirm = JOptionPane.showConfirmDialog(this, "Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    db.deleteData(conn, "partnership", condition + idMitraKerja);
                    reset.resetTextFields(this.getContentPane());
                    datatable();
                    tAlamat.setText("");
                } catch (SQLException ex) {
                    Logger.getLogger(dataPartnership.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
    }//GEN-LAST:event_bDeleteActionPerformed

